print "Informe o dia da semana de 1 a 7: "
dia = gets.chomp

if dia == '1'
    puts ("Domingo")
elsif dia == '2'
    puts ("Segunda")
elsif dia == '3'
    puts ("Terça")
elsif dia == '4'
    puts ("Quarta")
elsif dia == '5'
    puts ("Quinta")
elsif dia == '6'
    puts ("Sexta")
elsif dia == '7'
    puts ("Sábado")
end